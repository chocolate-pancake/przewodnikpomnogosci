let notPluralityHidden = false

function plurality(){
    let elements = document.querySelectorAll(".not-plurality");
    for(let i = 0; i < elements.length; i ++){
        if(!notPluralityHidden){
            elements[i].style.display = "none";
        }
        else{
            elements[i].style.display = "block";
        }
    }
    notPluralityHidden = !notPluralityHidden
}

